# 线程锁
lock = threading.Lock()

def safe_print(msg):
    with lock:
        print(msg)


def extract_channel(name, display_name, url, max_wait=30):
    """抓取单个频道 m3u8"""
    m3u8_urls = set()

    with sync_playwright() as p:
        browser = p.chromium.launch(headless=True)
        context = browser.new_context(
            user_agent="Mozilla/5.0 (Linux; Android 10; K) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/137.0.0.0 Mobile Safari/537.36",
            viewport={"width": 360, "height": 661},
            is_mobile=True
        )
        page = context.new_page()

        def handle_request(request):
            if ".m3u8" in request.url:
                # 过滤掉一些明显的非直播流片段（可选）
                m3u8_urls.add(request.url)

        page.on("request", handle_request)

        safe_print(f"[{name}] 正在访问页面...")
        try:
            page.goto(url, wait_until="domcontentloaded", timeout=30000)
        except Exception as e:
            safe_print(f"❌ [{name}] 页面加载失败: {e}")
            browser.close()
            return name, display_name, []

        # 点击播放
        selectors = ["button.custom-prism-play-control", "#player button", "video"]
        clicked = False
        for sel in selectors:
            try:
                page.wait_for_selector(sel, timeout=8000)
                page.click(sel)
                safe_print(f"[{name}] 点击成功: {sel}")
                clicked = True
                break
            except:
                continue

        if not clicked:
            try:
                page.evaluate("const v = document.querySelector('video'); if (v) v.play();")
            except:
                pass

        # 等待 m3u8
        start_time = time.time()
        while not m3u8_urls and time.time() - start_time < max_wait:
            time.sleep(0.5)

        browser.close()

    # 清理 URL
    results = []
    for m3u8 in m3u8_urls:
        new_m3u8 = urllib.parse.unquote(m3u8)
        # 清理多余参数
        new_m3u8 = re.sub(r'.*?(https://live-play-hls\.cctvnews\.cctv\.com.*?)(?:&stack.*)?$', r'\1', new_m3u8)
        # 去重并加入结果
        if new_m3u8 not in results:
            results.append(new_m3u8)

    safe_print(f"✅ [{name}] 完成，找到 {len(results)} 个直播源")
    return name, display_name, results


def generate_m3u(results, filename="cctv_live.m3u"):
    """生成 M3U 文件，包含所有找到的源"""
    with open(filename, "w", encoding="utf-8") as f:
        f.write("#EXTM3U\n")
        f.write("# Generated by CCTV Live Scraper\n\n")

        total_source_count = 0
        success_channel_count = 0
        
        for name, display_name, urls in results:
            if urls:
                success_channel_count += 1
                # 遍历该频道捕获到的所有 URL
                for i, stream_url in enumerate(urls):
                    total_source_count += 1
                    # 如果一个频道有多个源，为后续的源加上序号标识
                    suffix = f" - 源{i+1}" if len(urls) > 1 else ""
                    
                    f.write(f'#EXTINF:-1 tvg-id="{name}_{i}" tvg-name="{display_name}{suffix}" group-title="CCTV",{display_name}{suffix}\n')
                    f.write(f"{stream_url}\n\n")

    print(f"\n🎉 M3U 文件生成完成！")
    print(f"📊 统计：共抓取 {success_channel_count} 个频道，输出 {total_source_count} 个直播源")
    print(f"📁 文件保存为: {filename}")


def run_all_channels(max_workers=5, max_wait=30):
    """主函数"""
    all_results = []
    total = len(CHANNELS)

    print(f"\n🚀 启动抓取，共 {total} 个频道，最大并发 {max_workers} 个线程...\n")

    with ThreadPoolExecutor(max_workers=max_workers) as executor:
        futures = {
            executor.submit(extract_channel, name, disp_name, url, max_wait): name 
            for name, disp_name, url in CHANNELS
        }

        for future in as_completed(futures):
            try:
                name, display_name, urls = future.result()
                all_results.append((name, display_name, urls))
            except Exception as e:
                safe_print(f"❌ 线程异常: {e}")

    # 生成 M3U 文件（包含所有源）
    generate_m3u(all_results)

    # 控制台打印明细汇总
    print("\n" + "=" * 70)
    print(f"{'频道名称':<15} | {'源数量':<10} | {'主要地址'}")
    print("-" * 70)
    for name, display_name, urls in all_results:
        status = "✅" if urls else "❌"
        count = len(urls)
        first_url = urls[0] if urls else "N/A"
        print(f"{status} {display_name:<12} | {count:<8} | {first_url}")

    return all_results


if __name__ == "__main__":
    # 根据机器性能调整并发数
    MAX_WORKERS = 3      
