lock = threading.Lock()

def safe_print(msg):
    with lock:
        print(msg)

def clean_m3u8_url(raw_url):
    """清理并解码 m3u8 URL，保留所有有效参数"""
    decoded = urllib.parse.unquote(raw_url)
    # 仅移除明显的追踪参数，保留核心播放参数
    decoded = re.sub(r'[?&]stack=[^&]*', '', decoded)
    decoded = re.sub(r'[?&]track=[^&]*', '', decoded)
    return decoded

def extract_channel(name, display_name, url, max_wait=45):
    """抓取单个频道，返回所有捕获的 m3u8 链接（去重后）"""
    m3u8_urls = set()
    
    with sync_playwright() as p:
        browser = p.chromium.launch(headless=True)
        context = browser.new_context(
            user_agent="Mozilla/5.0 (Linux; Android 10; K) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/137.0.0.0 Mobile Safari/537.36",
            viewport={"width": 360, "height": 661},
            is_mobile=True
        )
        page = context.new_page()

        def handle_request(request):
            url_lower = request.url.lower()
            if ".m3u8" in url_lower:
                safe_print(f"🔥 [{name}] 捕获: {request.url[:120]}...")
                m3u8_urls.add(request.url)

        page.on("request", handle_request)

        safe_print(f"[{name}] 正在访问页面...")
        try:
            page.goto(url, wait_until="domcontentloaded", timeout=30000)
            # 额外等待 JS 初始化
            time.sleep(2)
        except Exception as e:
            safe_print(f"❌ [{name}] 页面加载失败: {e}")
            browser.close()
            return name, display_name, []

        # 尝试点击播放
        selectors = [
            "button.custom-prism-play-control",
            ".prism-play-btn",
            "#player button",
            "video",
            "[class*='play']"
        ]
        clicked = False
        for sel in selectors:
            try:
                if page.locator(sel).count() > 0:
                    page.locator(sel).first.click(timeout=5000)
                    safe_print(f"[{name}] 点击成功: {sel}")
                    clicked = True
                    break
            except Exception:
                continue

        if not clicked:
            try:
                page.evaluate("""() => {
                    const v = document.querySelector('video');
                    if (v) { v.play(); v.muted = true; }
                }""")
                safe_print(f"[{name}] 尝试直接播放 video 标签")
            except Exception as e:
                safe_print(f"[{name}] 自动播放失败: {e}")

        # 等待 m3u8 请求（延长等待时间，允许多个码率）
        safe_print(f"[{name}] 等待 m3u8 请求... (最多 {max_wait}s)")
        start_time = time.time()
        last_count = 0
        while time.time() - start_time < max_wait:
            time.sleep(1)
            if len(m3u8_urls) > last_count:
                last_count = len(m3u8_urls)
                safe_print(f"[{name}] 当前已捕获 {last_count} 个链接...")
            if len(m3u8_urls) >= 3:  # 通常有多个码率，捕获到几个就提前结束
                break

        browser.close()

    # 清理所有 URL
    results = [clean_m3u8_url(u) for u in m3u8_urls]
    # 按 URL 长度排序，通常短的是主控链接
    results.sort(key=len)
    
    safe_print(f"✅ [{name}] 完成，共 {len(results)} 个直播源")
    return name, display_name, results


def generate_m3u(results, filename="cctv_live_all.m3u"):
    """生成 M3U 文件，输出所有捕获的链接"""
    with open(filename, "w", encoding="utf-8") as f:
        f.write("#EXTM3U\n")
        f.write("# Generated by CCTV Live Scraper (Full Output)\n\n")

        success_count = 0
        total_links = 0
        
        for name, display_name, urls in results:
            if urls:
                success_count += 1
                # 输出该频道的所有链接，用码率/序号区分
                for idx, stream_url in enumerate(urls):
                    # 尝试从 URL 中提取清晰度信息
                    quality = "HD"
                    if "_4k" in stream_url.lower() or "4k" in name.lower():
                        quality = "4K"
                    elif "_2k" in stream_url.lower():
                        quality = "2K"
                    elif "1080" in stream_url:
                        quality = "1080P"
                    
                    label = f"{display_name}-{quality}" if idx == 0 else f"{display_name}-{quality}-ALT{idx}"
                    f.write(f'#EXTINF:-1 tvg-id="{name}" tvg-name="{display_name}" group-title="CCTV",{label}\n')
                    f.write(f"{stream_url}\n")
                    total_links += 1
                f.write("\n")

    safe_print(f"\n🎉 M3U 文件生成完成！")
    safe_print(f"📺 成功频道: {success_count}/{len(results)}")
    safe_print(f"🔗 总链接数: {total_links}")
    safe_print(f"📁 文件保存为: {filename}")
    return filename


def run_all_channels(max_workers=3, max_wait=45):
    """主函数"""
    all_results = []
    total = len(CHANNELS)

    safe_print(f"\n🚀 启动抓取，共 {total} 个频道，并发 {max_workers}...\n")

    with ThreadPoolExecutor(max_workers=max_workers) as executor:
        futures = {
            executor.submit(extract_channel, name, disp_name, url, max_wait): (name, disp_name)
            for name, disp_name, url in CHANNELS
        }

        for future in as_completed(futures):
            name, disp_name = futures[future]
            try:
                result = future.result()
                all_results.append(result)
            except Exception as e:
                safe_print(f"❌ [{name}] 线程异常: {e}")
                all_results.append((name, disp_name, []))

    # 按原始顺序排序
    order_map = {c[0]: i for i, c in enumerate(CHANNELS)}
    all_results.sort(key=lambda x: order_map.get(x[0], 999))

    # 生成 M3U
    generate_m3u(all_results)

    # 完整控制台输出
    safe_print("\n" + "=" * 80)
    safe_print("📊 完整抓取结果")
    safe_print("=" * 80)
    
    for name, display_name, urls in all_results:
        status = "✅" if urls else "❌"
        safe_print(f"\n{status} {display_name:12} ({len(urls)} 个源)")
        for i, u in enumerate(urls, 1):
            safe_print(f"   [{i}] {u}")

    safe_print("\n" + "=" * 80)
    return all_results


