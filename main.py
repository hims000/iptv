# 线程锁，防止控制台打印错乱
lock = threading.Lock()

def safe_print(msg):
    with lock:
        print(msg)

def extract_channel(name, display_name, url, max_wait=30):
    """抓取单个频道的所有 m3u8 地址"""
    m3u8_urls = set()

    with sync_playwright() as p:
        # 启动浏览器
        browser = p.chromium.launch(headless=True)
        context = browser.new_context(
            user_agent="Mozilla/5.0 (Linux; Android 10; K) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/137.0.0.0 Mobile Safari/537.36",
            viewport={"width": 360, "height": 661},
            is_mobile=True
        )
        page = context.new_page()

        # 拦截请求
        def handle_request(request):
            if ".m3u8" in request.url:
                # 解码 URL 并清洗冗余参数
                raw_url = urllib.parse.unquote(request.url)
                clean_url = re.sub(r'(https://live-play-hls\.cctvnews\.cctv\.com.*?\.m3u8).*', r'\1', raw_url)
                if clean_url:
                    m3u8_urls.add(clean_url)

        page.on("request", handle_request)

        try:
            page.goto(url, wait_until="domcontentloaded", timeout=30000)
            
            # 模拟点击播放以触发流请求
            selectors = ["button.custom-prism-play-control", "video", ".player-video"]
            for sel in selectors:
                try:
                    page.wait_for_selector(sel, timeout=5000)
                    page.click(sel)
                    break
                except:
                    continue
            
            # 等待流加载
            start_time = time.time()
            while not m3u8_urls and (time.time() - start_time < max_wait):
                time.sleep(1)
                
        except Exception as e:
            safe_print(f"❌ [{display_name}] 出错: {e}")
        finally:
            browser.close()

    # 按照 URL 长度排序，通常较长的包含完整 token
    final_urls = sorted(list(m3u8_urls), key=len, reverse=True)
    safe_print(f"✅ [{display_name}] 抓取完成，找到 {len(final_urls)} 条线路")
    return name, display_name, final_urls

def generate_m3u(results, filename="cctv_live.m3u"):
    """生成 M3U 文件，支持多线路相同 ID"""
    with open(filename, "w", encoding="utf-8") as f:
        f.write("#EXTM3U\n")
        f.write("# Generated by CCTV Scraper\n\n")

        channel_count = 0
        source_count = 0
        
        for name, display_name, urls in results:
            if not urls:
                continue
            
            channel_count += 1
            for stream_url in urls:
                source_count += 1
                # 保持相同的 id 和 name，播放器会自动聚合线路
                f.write(f'#EXTINF:-1 tvg-id="{name}" tvg-name="{display_name}" group-title="CCTV",{display_name}\n')
                f.write(f"{stream_url}\n\n")

    print("\n" + "="*50)
    print(f"🎉 任务完成！")
    print(f"📂 频道总数: {channel_count}")
    print(f"🔗 总线路数: {source_count}")
    print(f"📁 文件路径: {filename}")
    print("="*50)

def main():
    MAX_WORKERS = 4  # 建议不要设置太高，避免被封 IP 或内存溢出
    
    all_results = []
    print(f"🚀 开始并发抓取 (并发数: {MAX_WORKERS})...")

    with ThreadPoolExecutor(max_workers=MAX_WORKERS) as executor:
        future_to_channel = {
            executor.submit(extract_channel, c[0], c[1], c[2]): c[1] 
            for c in CHANNELS
        }

        for future in as_completed(future_to_channel):
            try:
                res = future.result()
                all_results.append(res)
            except Exception as e:
                print(f"线程执行失败: {e}")

    # 生成文件
    generate_m3u(all_results)

if __name__ == "__main__":
    main()
